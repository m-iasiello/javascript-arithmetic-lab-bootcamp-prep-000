//add
function add(a,b) {
  return a + b
}

//subtract
function subtract(a,b){
  return a - b 
}

//multiply
function multiply(a,b) {
  return a * b 
}

//divide
function divide(a,b) {
  return a / b 
}

//increment
function inc(n) {
  return n++
}

//decrement
function dec(n) {
  return n--
}

//make integer
function makeInt(n){
  return parseInt(n)
}

